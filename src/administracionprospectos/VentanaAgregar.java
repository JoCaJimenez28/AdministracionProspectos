    private void btnEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarActionPerformed
       
        ProspectosGS p = new ProspectosGS();
        DBConexion dbc = new DBConexion();
        
        //dbc.agregarProspecto("INSERT INTO prueba (id, nombre, correo) VALUES (null,'jose','aaaa');");
        //recuperamos los datos ingresados
        p=obtenerDatos();
        
        //lo guardamos en la bd
        String comandoInsert;
        comandoInsert = String.format("INSERT INTO tblprospectos "
                + "(id, nombre, apellidoPaterno, apellidoMaterno, calle, numero, colonia, codigoPostal, telefono, rfc)"
                + "VALUES (null, '%s', '%s', '%s','%s','%s','%s','%s','%s','%s');", 
                p.getNombre(),
                p.getApellidoP(),
                p.getApellidoM(),
                p.getCalle(),
                p.getNumero(),
                p.getColonia(),
                p.getCodigoPostal(),
                p.getTelefono(),
                p.getRfc());
        
        dbc.agregarProspecto(comandoInsert);
        
        //mensaje de que se guardó correctamente
        
        
        //Volvemos a la pantalla de inicio
        this.hide();
        VentanaEvaluar v =new VentanaEvaluar();
        v.setLocationRelativeTo(null);
        v.setVisible(true);
    }//GEN-LAST:event_btnEnviarActionPerformed

