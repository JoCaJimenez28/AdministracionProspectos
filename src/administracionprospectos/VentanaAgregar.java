    /**
     * @param args the command line arguments
     */
    public ProspectosGS obtenerDatos(){
        ProspectosGS prospecto = new ProspectosGS();
        
        if(
            txtNombre.getText()==null || 
            txtApellidoP.getText()==null ||
            txtApellidoM.getText()==null ||
            txtCalle.getText()==null ||
            txtNumero.getText()==null ||
            txtColonia.getText()==null ||
            txtCodigoPostal.getText()==null ||
            txtTelefono.getText()==null ||    
            txtRFC.getText()==null 
            ){
                JOptionPane.showMessageDialog(null, "Los campos con * no deben estar vacios");
                return null;
        }
        else{
        prospecto.setNombre(txtNombre.getText());
        prospecto.setApellidoP(txtApellidoP.getText());
        prospecto.setApellidoM(txtApellidoM.getText());
        prospecto.setCalle(txtCalle.getText());
        prospecto.setNumero(txtNumero.getText());
        prospecto.setColonia(txtColonia.getText());
        prospecto.setCodigoPostal(txtCodigoPostal.getText());
        prospecto.setTelefono(txtTelefono.getText());
        prospecto.setRfc(txtRFC.getText());
        
        return prospecto;
        }
    }
    
